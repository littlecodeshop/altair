void printString(char *s)
{
    glPushAttrib (GL_LIST_BIT);
    glListBase(fontOffset);
    glCallLists((GLsizei) strlen(s), GL_UNSIGNED_BYTE, (GLubyte *) s);
    glPopAttrib ();
}

void printCursor()
{

    char c = 120;
    glRasterPos2i(20+(cursx*10),600-(cursy*20));
    glPushAttrib (GL_LIST_BIT);
    glListBase(fontOffset);
    glCallLists(1, GL_UNSIGNED_BYTE, (GLubyte *)&c );
    glPopAttrib ();

}

