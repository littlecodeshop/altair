GLuint fontOffset;

void makeRasterFont(void)
{
    //read the font from the freaking file

    GLuint i, j;
    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);

    fontOffset = glGenLists (128);
    for (i = 0,j = ' '; i < 95; i++,j++) {
	glNewList(fontOffset + j, GL_COMPILE);
	glBitmap(8, 20, 0.0, 2.0, 10.0, 0.0, letters[i]);
	glEndList();
    }
    glNewList(fontOffset + 120, GL_COMPILE);
    glBitmap(8, 20, 0.0, 2.0, 10.0, 0.0, cursor);
    glEndList();
}

